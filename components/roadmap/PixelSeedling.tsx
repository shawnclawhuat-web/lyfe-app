interface Props {
    size?: number; // scale multiplier (default 1)
    reducedMotion?: boolean;
}

function PixelSeedling({ size = 1, reducedMotion }: Props) {
    const px = PX * size;
    const gridW = GRID_W * px;
    const gridH = GRID_H * px;

    const scale = useSharedValue(1);
    const translateY = useSharedValue(0);

    useEffect(() => {
        if (reducedMotion) return;
        // Gentle breathing pulse
        scale.value = withRepeat(
            withSequence(
                withTiming(1.04, { duration: 1800, easing: Easing.inOut(Easing.sin) }),
                withTiming(1.0, { duration: 1800, easing: Easing.inOut(Easing.sin) }),
            ),
            -1,
            false,
        );
        // Slow vertical bob
        translateY.value = withRepeat(
            withSequence(
                withTiming(-3, { duration: 1800, easing: Easing.inOut(Easing.sin) }),
                withTiming(0, { duration: 1800, easing: Easing.inOut(Easing.sin) }),
            ),
            -1,
            false,
        );
    }, [scale, translateY, reducedMotion]);

    const animatedStyle = useAnimatedStyle(() => ({
        transform: [{ scale: scale.value }, { translateY: translateY.value }],
    }));

    return (
        <View style={[styles.container, { width: gridW, height: gridH }]}>
            <Animated.View style={[styles.grid, { width: gridW, height: gridH }, animatedStyle]}>
                {BEAN_PIXELS.map(([col, row, color], i) => (
                    <View
                        key={i}
                        style={{
                            position: 'absolute',
                            left: col * px,
                            top: row * px,
                            width: px,
                            height: px,
                            backgroundColor: color,
                            borderRadius: px / 4,
                        }}
                    />
                ))}
            </Animated.View>
        </View>
    );
}

const styles = StyleSheet.create({
    container: {
        alignItems: 'center',
        justifyContent: 'center',
    },
    grid: {
        position: 'relative',
    },
});

export default React.memo(PixelSeedling);
